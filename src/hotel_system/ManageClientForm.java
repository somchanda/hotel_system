    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
      
        try{
                int id = Integer.valueOf(txtId.getText());
                if (client.removeClient(id)) {
                    JOptionPane.showMessageDialog(rootPane, "Client Removed", "Remove client", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Client not Remove", "Remove client error", JOptionPane.INFORMATION_MESSAGE);
                }
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(rootPane, ex.getMessage()+"Please select row!","Error Remove", JOptionPane.ERROR_MESSAGE);
        }
        

    }//GEN-LAST:event_btnRemoveActionPerformed

