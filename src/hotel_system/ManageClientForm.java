    private void jTableClientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableClientMouseClicked
        DefaultTableModel model = (DefaultTableModel) jTableClient.getModel();
        //get select row index
        int rIndex = jTableClient.getSelectedRow();
        //display data
        txtId.setText(model.getValueAt(rIndex, 0).toString());
        txtFirsName.setText(model.getValueAt(rIndex, 1).toString());
        txtLastName.setText(model.getValueAt(rIndex, 2).toString());
        txtPhone.setText(model.getValueAt(rIndex, 3).toString());
        txtMail.setText(model.getValueAt(rIndex, 4).toString());

    }//GEN-LAST:event_jTableClientMouseClicked

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int id = 0;
        String fname = txtFirsName.getText();
        String lname = txtLastName.getText();
        String phone = txtPhone.getText();
        String mail = txtMail.getText();
        if (fname.trim().equals("") || lname.trim().equals("") || phone.trim().equals("") || mail.trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Required field", "Emty field!", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                id = Integer.valueOf(txtId.getText());
                if (client.updateClient(id, fname, lname, phone, mail)) {
                    JOptionPane.showMessageDialog(rootPane, "Client update succesful", "Update client", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Client not Update", "Update client error", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage() + "Enter the client id", "Update client error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

