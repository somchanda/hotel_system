    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String fname = txtFirsName.getText();
        String lname = txtLastName.getText();
        String phone = txtPhone.getText();
        String mail = txtMail.getText();
        if (fname.trim().equals("") || lname.trim().equals("") || phone.trim().equals("") || mail.trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please input information!", "Emty field!", JOptionPane.WARNING_MESSAGE);
        } else {
            if (client.addClient(fname, lname, phone, mail)) {
                JOptionPane.showMessageDialog(rootPane, "Client add succesful", "Add client", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Client not add", "Add client error", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

